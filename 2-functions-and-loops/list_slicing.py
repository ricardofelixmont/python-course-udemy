# Imagine you've got the list below:
friends = ['Ewerton', 'Lucas', 'Carlos', 'Moringa', 'Ricardo Cabeleira']

print(friends[2:])
print(friends[:2])
print(friends[-1]) 
print(friends[-1:-3]) # It returns a void list
print(friends[-3:-1]) # This is the way we slice a 'reverse list', python reads from left to right
print(friends[-3:])   # It shows from -3 position to the end of the list
print(friends[:-3])
